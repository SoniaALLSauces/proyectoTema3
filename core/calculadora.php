    /*
     * function sumar()
     * param $n1 primer valor
     * param &n2 segundo valor
     * return la suma de los valores recibidos como parametro
     */
    function suma ($n1,$n2){
        return $n1+$n2;
    }
    
    /*
     * function restar()
     * param $n1 primer valor
     * param &n2 segundo valor
     * return la resta de los valores recibidos como parametro
     */
    function resta ($n1,$n2){
        return $n1-$n2;
    }
    
    /*
     * function multiplicacion()
     * param $n1 primer valor
     * param &n2 segundo valor
     * return la multiplicacion de los valores recibidos como parametro
     */
    function multiplicacion ($n1,$n2){
        return $n1*$n2;
    }
    
    /*
     * function dividir()
     * param $n1 primer valor
     * param &n2 segundo valor
     * return la division de los valores recibidos como parametro
     */
    function division ($n1,$n2){
        return $n1/$n2;
    }

